
public class Persona {
    public String cedula;
    public String nombre;
    public String provincia;
    public String gender;
    public String country;
    public String email;

    public Persona() {
    }

    public Persona(String cedula, String nombre, String provincia, String gender, String country, String email) {
        this.cedula = cedula;
        this.nombre = nombre;
        this.provincia = provincia;
        this.gender = gender;
        this.country = country;
        this.email = email;
    }
}
